package com.po;

public class Student {
	int sid;
	String sname;
	int age;
	float height;
	public int getSid() {
		return sid;
	}
	public void setSid(int i) {
		this.sid = i;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public float getHeight() {
		return height;
	}
	public void setHeight(float height) {
		this.height = height;
	}

}
